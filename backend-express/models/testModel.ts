import { Schema } from "mongoose";
import { model } from "mongoose";

let testSchema = new Schema({
  title: String,
  author: String,
  body: String,
  comments: [{ body: String, date: Date }],
  date: { type: Date, default: Date.now },
  hidden: Boolean,
  meta: {
    votes: Number,
    favs: Number,
  },
});

let testModel = model("test", testSchema);
